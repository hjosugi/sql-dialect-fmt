const BODY_DELIMITER_RULES = [
  dollarQuotedBody,
];

module.exports = grammar({
  name: 'snowflake',

  extras: $ => [
    /[\s\uFEFF\u2060]+/,
    $.comment,
  ],

  word: $ => $.identifier,

  rules: {
    source_file: $ => repeat($.statement),

    // A top-level statement: a run of tokens up to (and including) its `;` terminator. The last
    // statement in a script may omit the terminator, and a bare `;` is an empty statement. This is
    // the first structural layer over the flat token stream — enough for statement-level folds and
    // navigation without committing to a full expression grammar.
    statement: $ => choice(
      prec.right(seq(repeat1($._token), optional(';'))),
      ';',
    ),

    _token: $ => choice(
      $.stage_reference,
      $.keyword,
      $.type,
      $.dollar_string,
      $.string,
      $.quoted_identifier,
      $.number,
      $.variable,
      $.identifier,
      $.operator,
      $.punctuation,
    ),

    comment: _ => token(prec(10, choice(
      seq('--', /[^\r\n]*/),
      seq('//', /[^\r\n]*/),
      seq('/*', repeat(choice(/[^*]/, seq('*', /[^/]/))), repeat('*'), '/'),
    ))),

    stage_reference: _ => token(prec(4, seq(
      '@',
      repeat1(choice(
        /[A-Za-z0-9_$%~./:=+-]+/,
        seq('"', repeat(choice(/[^"]/, '""')), '"'),
      )),
    ))),

    keyword: _ => token(prec(2, choice(...KEYWORDS.map(word)))),

    type: _ => token(prec(2, choice(...TYPES.map(word)))),

    identifier: _ => /[A-Za-z_][A-Za-z0-9_$]*/,

    quoted_identifier: _ => token(seq(
      '"',
      repeat(choice(/[^"]/, '""')),
      '"',
    )),

    string: _ => token(seq(
      "'",
      repeat(choice(/[^'\\]/, /\\./, "''")),
      "'",
    )),

    dollar_string: _ => token(choice(...BODY_DELIMITER_RULES.map(rule => rule()))),

    number: _ => token(choice(
      /\d+\.\d*([eE][+-]?\d+)?/,
      /\.\d+([eE][+-]?\d+)?/,
      /\d+([eE][+-]?\d+)?/,
    )),

    variable: _ => token(seq('$', choice(/[0-9]+/, /[A-Za-z_][A-Za-z0-9_$]*/))),

    operator: _ => token(prec(3, choice(
      '->>',
      '|>',
      '::',
      ':=',
      '=>',
      '->',
      '||',
      '<=>',
      '<=',
      '>=',
      '<>',
      '!=',
      '=',
      '<',
      '>',
      '+',
      '-',
      '*',
      '/',
      '%',
      ':',
      '|',
      '&',
      '^',
      '~',
      '@',
      '?',
      '!',
    ))),

    punctuation: _ => token(choice(
      '(',
      ')',
      '[',
      ']',
      '{',
      '}',
      ',',
      '.',
    )),
  },
});

function word(value) {
  const escaped = value.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
  return new RegExp(escaped.replace(/[A-Za-z]/g, char => `[${char.toLowerCase()}${char.toUpperCase()}]`));
}

function dollarQuotedBody() {
  return seq(
    '$$',
    repeat(choice(/[^$]/, seq('$', /[^$]/))),
    '$$',
  );
}
